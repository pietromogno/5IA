    private class Ascoltatore extends Thread {

        @Override
        public void run() {
            boolean continua = true;
            while (continua) {
                try {
                    String op = in.readLine();
                    if (op.equals("Errore")) {
                        labelErrori.setText("Contatto non valido");
                    } else if (op.equals("AggiungiChat")) {
                        JLabel labelChat = new JLabel(in.readLine());
                        labelChat.setAlignmentX(Component.CENTER_ALIGNMENT);
                        labelChat.addMouseListener(new AscoltatorePerChat(Integer.parseInt(in.readLine())));
                        panelChat.add(labelChat);
                        setVisible(true);
                    }else if(op.equals("Chat")){
                        continua=false;
                        dispose();
                        ChatView v=new ChatView(in,out,Integer.parseInt(in.readLine()));
                        v.setVisible(true);
                    }else if (op.equals("")) {
                        continua = false;
                    }
                } catch (IOException ex) {
                    Logger.getLogger(UserView.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }

